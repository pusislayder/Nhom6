    public void getBook(){
        tableModel.setRowCount(0);
        if(this.listSach.size()>0)
            this.listSach.clear();
        this.listSach = this.sachController.getListSach();
        if(listSach.size() > 0){
            for(SachModelGiangVanDat list : listSach ){
                Object[] s = new Object[]{
                    list.getTenSach(), list.getTheLoai(), list.getNamXuatBan(), list.getNgayNhap(), list.getGiaTien()
                };
                tableModel.addRow(s);
            }
        }
    }
    
