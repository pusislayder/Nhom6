    private void jmSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmSuaActionPerformed
        // TODO add your handling code here:
        if(current!=-1){
            SachModelGiangVanDat item = this.listSach.get(current);
            int id = item.getId();
            String TenSach = item.getTenSach();
            String TheLoai = item.getTheLoai();
            Date NamXuatBan = item.getNamXuatBan();
            Date NgayNhap = item.getNgayNhap();
            int GiaTien = item.getGiaTien();
            frame = new editSachGiangVanDat(id,TenSach,TheLoai,NamXuatBan,NgayNhap,GiaTien);
            frame.setLocationRelativeTo(null);
            frame.setResizable(false);
            frame.setVisible(true);
        }
    }//GEN-LAST:event_jmSuaActionPerformed

    private void jmXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmXoaActionPerformed
        // TODO add your handling code here:
        if(current!=-1){
            SachModelGiangVanDat item = this.listSach.get(current);
            int option = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn sửa đổi chứ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                boolean check = sachController.deleteSach(item.getId());
                if(check){
                    getBook();
                    JOptionPane.showMessageDialog(null, "Đã xóa thành công!");
                }else{
                    JOptionPane.showMessageDialog(null, "Lỗi không mong muốn! Vui lòng thao tác lại");
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn dữ liệu để thao tác!");
        }
    }//GEN-LAST:event_jmXoaActionPerformed

